def generate_ip(comp_casns):
    n = len(comp_casns)
    comp_to_pos = {casn: counter for counter, casn in enumerate(comp_casns)}
    g = np.zeros((n, n))
    alpha = np.zeros((n, n))
    combs = [i for i in combinations(comp_casns, 2)]
    for (casn1, casn2) in combs:
        lookup = __NRTL_IP[(casn1, casn2)]
        pos1 = comp_to_pos[casn1]
        pos2 = comp_to_pos[casn2]
        g[pos1, pos2] = lookup[2]
        g[pos2, pos1] = lookup[3]
        alpha[pos1, pos2] = lookup[4]
        alpha[pos2, pos1] = lookup[4]

    return g, alpha
